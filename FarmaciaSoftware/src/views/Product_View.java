    /**
     * Function gets all the text from the new product view, insert product view
     * and update/delete product view and sets local variables of that instance.
     * 
     * @access public
     * @return void 
     */
    public void setProduct() {

        String prodName = productName.getText();
        int prodID = Integer.parseInt(productCode.getText());
        double prodFinal = Double.parseDouble(productFinalPrice.getText());
        String prodUnity = (String)productUnity.getSelectedItem();
        String prodDescription = productDescription.getText();
        double priceIncash = Double.parseDouble(priceInCash.getText());
        double priceInterm = Double.parseDouble(priceInTerm.getText());

        this.prod = new Product(prodID, prodName, prodUnity,priceIncash, priceInterm, prodFinal, prodDescription);



    }
    
     /**
     * 
     * @return the current product object to be used 
     */
    public Product getProduct() {

        return this.prod;
    }

